public class FindOddOrEven {

    public static void findNumber() {

        for (int i = 0; i <= 10; i++) {
            if (i % 2 == 0) {
                System.out.println("Even");
            } else {
                System.out.println("ODD");
            }
        }

    }

    public static void main(String[] args) {
        findNumber();

    }

}
